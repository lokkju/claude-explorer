def build_cc_user_entry(
    *,
    uuid: str,
    text: str,
    session_id: str = "sess",
    cwd: str = "/tmp/p",
    git_branch: str = "main",
    timestamp: str = _DEFAULT_MSG_CREATED_AT,
    parent_uuid: str | None = None,
) -> dict[str, Any]:
    """One ``type=user`` row in Claude Code's JSONL wire format.

    Matches the shape that ``backend.claude_code_reader`` parses:
    ``type``, ``uuid``, ``sessionId``, ``cwd``, ``gitBranch``,
    ``timestamp``, ``parentUuid``, ``message: {role, content}``.
    """
    return {
        "type": "user",
        "uuid": uuid,
        "parentUuid": parent_uuid,
        "sessionId": session_id,
        "cwd": cwd,
        "gitBranch": git_branch,
        "timestamp": timestamp,
        "message": {"role": "user", "content": text},
    }


def build_cc_assistant_entry(
    *,
    uuid: str,
    msg_id: str,
    text: str,
    session_id: str = "sess",
    timestamp: str = _DEFAULT_MSG_CREATED_AT,
    parent_uuid: str | None = None,
    content: list[dict[str, Any]] | None = None,
    model: str = _DEFAULT_MODEL,
) -> dict[str, Any]:
    """One ``type=assistant`` row in Claude Code's JSONL wire format.

    ``content`` lets callers pass tool-use / tool-result blocks
    explicitly. When omitted, defaults to a single ``text`` block
    holding ``text``.
    """
    effective_content = (
        [dict(c) for c in content]
        if content is not None
        else [{"type": "text", "text": text}]
    )
    return {
        "type": "assistant",
        "uuid": uuid,
        "parentUuid": parent_uuid,
        "sessionId": session_id,
        "timestamp": timestamp,
        "message": {
            "id": msg_id,
            "role": "assistant",
            "model": model,
            "content": effective_content,
        },
    }


def write_cc_jsonl(path: Path, entries: list[dict[str, Any]]) -> Path:
    """Persist a Claude Code JSONL session at ``path``.

    Each entry becomes one line. Creates parent dirs. Returns the
    written path.
    """
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        for entry in entries:
            f.write(json.dumps(entry) + "\n")
    return path
