def test_marker_args_does_not_overmatch_across_concatenated_blocks(
    tmp_path: Path,
) -> None:
    """V1 polish (2026-05-13, Fix 1) — Audit invariant #2:

    The user-reported bug: in session `76fe578b-...`, some `/exit`
    markers for 2026-04-21 and 2026-04-24 rendered as
    `'</command-args>\\n<command-name>/exit</command-name>...'` — the
    greedy `<command-args>(.*)</command-args>` regex was matching from
    the FIRST `<command-args>` to the LAST `</command-args>` in a row
    containing multiple concatenated command triplets, swallowing the
    intermediate `</command-args>` + `<command-name>` boilerplate as
    "args body."

    The fix (current, 2026-05-14): non-greedy body with a
    trailing-context lookahead
    `(.*?)</command-args>(?=\\s*(?:<command-name>|$))`. Multi-block
    rows correctly truncate at the FIRST `</command-args>` whose match
    is followed by the next `<command-name>` tag (the lookahead's
    primary anchor), so the second block's XML never leaks into the
    first block's args body.

    Bidirectional check: a row containing two glued-together triplets
    must NOT produce a marker whose `text` contains the raw XML of the
    second block.
    """
