"""V1 polish (2026-05-12): collapse `<local-command-caveat>`/<command-name>/
`<local-command-stdout>` triplets that Claude Code emits around slash commands.

User-visible regression: opening a CC session whose first conversational turns
were just `/exit` showed three raw-XML "messages" at the top of the stream
before any real content. We fold those into a single muted `"Session: /foo"`
marker so the user sees the real conversation immediately.

The collapse lives in
:func:`backend.claude_code_reader._collapse_local_command_triplets` and is
called at the end of :func:`read_claude_code_conversation` after streaming
chunks are merged. These tests pin the contract bidirectionally:

  * **Positive direction** — a triplet followed by a real user prompt must
    collapse to ``[marker, real_user, ...]`` (NOT ``[caveat, command, stdout,
    real_user, ...]``).
  * **Negative direction** — a real user message that just *mentions* the
    caveat-text outside the XML wrapper must NOT be collapsed.
  * **Orphan / phantom** — a session that contains nothing but boilerplate
    must still render *something* (the marker), so the view is never blank
    and the phantom-carve-out invariant
    (``test_phantom_caveat_carveout.py:140``: ``len(chat_messages) >= 2``
    when paired with a real message) is preserved.
  * **/compact passthrough** — the existing compact-markers system owns
    ``/compact`` triplets; this collapser must leave them alone.
"""

from __future__ import annotations

import json
from pathlib import Path

from backend.claude_code_reader import read_claude_code_conversation


_CAVEAT_TEXT = (
    "<local-command-caveat>Caveat: The messages below were generated by the "
    "user while running local commands. DO NOT respond to these messages or "
    "otherwise consider them in your response unless the user explicitly "
    "asks you to.</local-command-caveat>"
)


def _write_jsonl(path: Path, entries: list[dict]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        for entry in entries:
            f.write(json.dumps(entry) + "\n")


def _user(uuid: str, text: str, *, ts: str, parent: str | None = None) -> dict:
    return {
        "type": "user",
        "uuid": uuid,
        "parentUuid": parent,
        "sessionId": "sess",
        "cwd": "/tmp/p",
        "timestamp": ts,
        "message": {"role": "user", "content": text},
    }


def _assistant(uuid: str, msg_id: str, text: str, *, ts: str, parent: str | None = None) -> dict:
    return {
        "type": "assistant",
        "uuid": uuid,
        "parentUuid": parent,
        "sessionId": "sess",
        "timestamp": ts,
        "message": {
            "id": msg_id,
            "role": "assistant",
            "model": "claude-sonnet-4-6",
            "content": [{"type": "text", "text": text}],
        },
    }


def test_exit_triplet_collapses_to_single_marker_then_real_message_first(tmp_path: Path) -> None:
    """The canonical bug report.

    JSONL stream is exactly what session `76fe578b-...` had at the top:
    a `<local-command-caveat>` row, a `<command-name>/exit</command-name>`
    row, a `<local-command-stdout>Goodbye!</local-command-stdout>` row,
    then the user's real first prompt. After our collapser the rendered
    stream must start with a single "Session: /exit" marker followed by
    the real prompt — NOT three raw-XML rows.
    """
    path = tmp_path / "projects" / "-tmp-p" / "sess.jsonl"
    _write_jsonl(path, [
        _user("u1", _CAVEAT_TEXT, ts="2026-04-19T01:31:14Z"),
        _user(
            "u2",
            "<command-name>/exit</command-name>\n"
            "            <command-message>exit</command-message>\n"
            "            <command-args></command-args>",
            ts="2026-04-19T01:31:14Z",
            parent="u1",
        ),
        _user(
            "u3",
            "<local-command-stdout>Goodbye!</local-command-stdout>",
            ts="2026-04-19T01:31:14Z",
            parent="u2",
        ),
        _user(
            "u4",
            "I don't think we've pushed this repo yet. Check for me.",
            ts="2026-04-19T01:53:49Z",
            parent="u3",
        ),
        _assistant(
            "a1",
            "asst_1",
            "Let me check the git remote configuration.",
            ts="2026-04-19T01:53:54Z",
            parent="u4",
        ),
    ])

    detail = read_claude_code_conversation(path)
    assert detail is not None
    messages = detail["chat_messages"]

    # Triplet (3 rows) + real_user + assistant = 5 rows pre-collapse, 3 post.
    assert len(messages) == 3, (
        f"expected triplet to collapse to 1 marker + real user + assistant; "
        f"got {len(messages)} messages: {[m.get('text', '')[:60] for m in messages]}"
    )

    # Marker must be at index 0.
    marker = messages[0]
    assert marker.get("is_command_marker") is True, (
        f"first message must be the synthetic command marker; got: {marker!r}"
    )
    assert marker.get("sender") == "human"
    assert marker.get("text") == "Session: /exit", (
        f"marker text should be 'Session: /exit'; got: {marker.get('text')!r}"
    )
    # Marker must NOT contain raw XML — that's the whole point of the fix.
    assert "<local-command-caveat>" not in marker.get("text", "")
    assert "<command-name>" not in marker.get("text", "")
    assert "<local-command-stdout>" not in marker.get("text", "")

    # The real user prompt must be at index 1, intact.
    real_user = messages[1]
    assert real_user.get("sender") == "human"
    assert "pushed this repo" in real_user.get("text", "")
    assert not real_user.get("is_command_marker", False)

    # Assistant follows.
    assert messages[2].get("sender") == "assistant"


