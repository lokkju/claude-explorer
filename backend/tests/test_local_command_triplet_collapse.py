def test_argful_command_marker_carries_args_as_text_and_slash_command(
    tmp_path: Path,
) -> None:
    """A /coding marker with non-empty <command-args> must put the args
    body into `text` and the command name into `slash_command`.

    This is the canonical bug fix: the original collapser produced
    `text="Session: /coding"` and discarded the args. Post-fix the args
    body IS the text, and the badge gets the command name via the new
    `slash_command` field.

    V1 polish (2026-05-13, Fix 2): `is_command_marker` is now FALSE on
    argful markers (spec invariant X1: argful is never a marker). The
    `text` body IS real user content; downstream consumers that filter
    on `is_command_marker` (export, prelude pass, future "skip markers"
    affordance) MUST NOT suppress argful bubbles. The badge is driven
    by `slash_command`, not by `is_command_marker`, so it still renders.
    """
    path = tmp_path / "projects" / "-tmp-p" / "sess.jsonl"
    _write_jsonl(path, [
        _user("u1", _CAVEAT_TEXT, ts="2026-04-19T01:31:14Z"),
        _user(
            "u2",
            "<command-name>/coding</command-name>\n"
            "            <command-message>coding</command-message>\n"
            "            <command-args>Double-check your plan with the "
            "LLM council.</command-args>",
            ts="2026-04-19T01:31:14Z",
            parent="u1",
        ),
        # No <local-command-stdout> row — /coding is interactive, so CC
        # routes the args straight to Claude and skips the stdout frame.
        _assistant(
            "a1",
            "asst_1",
            "Sure, let me run the council.",
            ts="2026-04-19T01:31:20Z",
            parent="u2",
        ),
    ])

    detail = read_claude_code_conversation(path)
    assert detail is not None
    messages = detail["chat_messages"]

    assert len(messages) == 2, (
        f"expected [marker, assistant]; got {len(messages)} messages: "
        f"{[m.get('text', '')[:60] for m in messages]}"
    )

    marker = messages[0]
    # Spec invariant X1 (V1 polish 2026-05-13): argful is never a marker.
    assert marker.get("is_command_marker") is False, (
        f"argful /coding marker must have is_command_marker=False (spec X1); "
        f"got {marker.get('is_command_marker')!r}"
    )
    assert marker.get("slash_command") == "/coding", (
        f"slash_command must carry the command name; got {marker.get('slash_command')!r}"
    )
    # The ENTIRE point of the fix: the user's prompt body is preserved
    # as the marker's text. Raw XML is gone.
    assert marker.get("text") == "Double-check your plan with the LLM council.", (
        f"marker text must be the args body verbatim (stripped); got: "
        f"{marker.get('text')!r}"
    )
    assert "<command-args>" not in marker.get("text", "")
    assert "<command-name>" not in marker.get("text", "")
    # The content list mirrors the text so MarkdownRenderer sees a
    # single text block with the args body.
    assert marker.get("content") == [
        {"type": "text", "text": "Double-check your plan with the LLM council."}
    ]


