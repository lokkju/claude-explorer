# These tests pin the contract on the four observable behaviors:
#   1. Argful marker carries args as text + slash_command field.
#   2. Argless marker keeps the "Session: /foo" label AND sets slash_command.
#   3. Orphan caveat (no command-name row) has slash_command=None.
