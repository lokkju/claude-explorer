"""V1 polish (2026-05-12, council round 2): canned-response fold + leading-prelude flag.

User-visible regression: even after `_collapse_local_command_triplets` turned each
`/exit` triplet into a single `is_command_marker: True` marker, the JSONL still
contained the orphan assistant row CC emits as its reply to /exit — an
`assistant` message whose text is literally `"No response requested."`. Result:
the top of session `76fe578b-...` rendered as

  [marker, "No response requested.", marker, "No response requested.", real_user]

which is confusing. We now (Part A) absorb each canned-response assistant into the
preceding marker, and (Part B) flag the leading run of markers with `is_prelude:
True` so the frontend can hide them behind a "Session prelude: N earlier /exit
runs (show)" affordance.

These tests pin the contract bidirectionally:

  * **Part A positive** — marker + `"No response requested."` assistant collapses
    to 1 message; the marker carries `assistant_canned_response_consumed: True`;
    the absorbed assistant is gone from `chat_messages`.
  * **Part A counter (text mismatch)** — marker + ANY other assistant text leaves
    both messages untouched. The fold is exact-string, conservative-by-design;
    if CC ever changes its canned wording the new bubble renders verbatim.
  * **Part A counter (no marker)** — assistant `"No response requested."` NOT
    preceded by a marker (e.g. preceded by a real user message) is NOT folded.
    Otherwise legitimate "No response requested." replies (rare but possible)
    would be silently erased.
  * **Part A counter (tool blocks)** — marker + assistant whose text happens to
    equal the canned string but whose content carries `tool_use` is NOT folded.
    Tool history is real data we must preserve.
  * **Part A multi-canned run** — marker + TWO consecutive canned-response
    assistants (CC glitch) absorbs both. The first non-canned successor's
    `parent_message_uuid` is remapped past both absorbed uuids.
  * **Part A parent-chain bridge** — the successor's `parent_message_uuid` is
    remapped from the absorbed assistant's uuid to the marker's uuid.
  * **Part B positive** — 2 leading markers + real user → `prelude_hidden_count=2`,
    both markers have `is_prelude: True`, real user does NOT.
  * **Part B counter (no prelude)** — session starting with a real user message
    → `prelude_hidden_count=0`, no `is_prelude` flags anywhere.
  * **Part B counter (mid-stream marker)** — a marker AFTER the first real user
    is NOT flagged. Only the LEADING run counts.
  * **Integration** — full pipeline on canonical session shape (2 × triplet +
    2 × canned-response + real user) → 1 visible non-prelude message,
    `prelude_hidden_count=2`.
"""

from __future__ import annotations

import json
from pathlib import Path

from backend.claude_code_reader import (
    _fold_canned_assistant_responses_into_marker,
    _flag_leading_prelude_markers,
    read_claude_code_conversation,
)


_CAVEAT_TEXT = (
    "<local-command-caveat>Caveat: The messages below were generated by the "
    "user while running local commands. DO NOT respond to these messages or "
    "otherwise consider them in your response unless the user explicitly "
    "asks you to.</local-command-caveat>"
)


def _write_jsonl(path: Path, entries: list[dict]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        for entry in entries:
            f.write(json.dumps(entry) + "\n")


def _user(uuid: str, text: str, *, ts: str, parent: str | None = None, session_id: str = "sess") -> dict:
    return {
        "type": "user",
        "uuid": uuid,
        "parentUuid": parent,
        "sessionId": session_id,
        "cwd": "/tmp/p",
        "timestamp": ts,
        "message": {"role": "user", "content": text},
    }


def _assistant(
    uuid: str,
    msg_id: str,
    text: str,
    *,
    ts: str,
    parent: str | None = None,
    content: list[dict] | None = None,
    session_id: str = "sess",
) -> dict:
    return {
        "type": "assistant",
        "uuid": uuid,
        "parentUuid": parent,
        "sessionId": session_id,
        "timestamp": ts,
        "message": {
            "id": msg_id,
            "role": "assistant",
            "model": "claude-sonnet-4-6",
            "content": content if content is not None else [{"type": "text", "text": text}],
        },
    }


