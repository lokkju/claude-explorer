def _marker(uuid: str, parent: str | None = None) -> dict:
    """Build a synthetic marker matching `_collapse_local_command_triplets`'s shape."""
    return {
        "uuid": uuid,
        "sender": "human",
        "text": "Session: /exit",
        "content": [{"type": "text", "text": "Session: /exit"}],
        "created_at": "2026-04-19T01:31:14Z",
        "updated_at": "2026-04-19T01:31:14Z",
        "truncated": False,
        "parent_message_uuid": parent,
        "attachments": [],
        "files": [],
        "is_command_marker": True,
    }


def _post_msg(uuid: str, sender: str, text: str, *, parent: str | None) -> dict:
    return {
        "uuid": uuid,
        "sender": sender,
        "text": text,
        "content": [{"type": "text", "text": text}],
        "created_at": "2026-04-19T01:53:49Z",
        "updated_at": "2026-04-19T01:53:49Z",
        "truncated": False,
        "parent_message_uuid": parent,
        "attachments": [],
        "files": [],
    }


def test_fold_canned_assistant_after_marker_absorbs_and_flags() -> None:
    """Positive Part A: marker + 'No response requested.' assistant → 1 msg."""
    marker = _marker("m1", parent=None)
    canned = _post_msg("a1", "assistant", "No response requested.", parent="m1")
    real = _post_msg("u1", "human", "Real follow-up.", parent="a1")

    out = _fold_canned_assistant_responses_into_marker([marker, canned, real])

    assert len(out) == 2, f"expected [marker, real]; got {[m.get('text') for m in out]}"
    assert out[0]["uuid"] == "m1"
    assert out[0]["is_command_marker"] is True
    assert out[0]["assistant_canned_response_consumed"] is True, (
        "fold MUST set assistant_canned_response_consumed=True on the marker"
    )
    # Parent-chain bridge: real's parent_message_uuid was 'a1' (the absorbed
    # assistant); now remapped to 'm1'.
    assert out[1]["parent_message_uuid"] == "m1", (
        f"successor's parent_message_uuid must be remapped to marker uuid; "
        f"got {out[1]['parent_message_uuid']!r}"
    )
    assert out[1]["text"] == "Real follow-up."


def test_fold_does_not_fire_when_text_differs() -> None:
    """Negative Part A: assistant text other than the exact canned string is NOT folded.

    Conservative-by-design: if CC ever changes the wording (e.g. 'No response
    needed.') the new text renders verbatim — visible, fixable, no silent erasure.
    """
    marker = _marker("m1")
    other = _post_msg("a1", "assistant", "Sure, here's a thought.", parent="m1")

    out = _fold_canned_assistant_responses_into_marker([marker, other])

    assert len(out) == 2, "no fold should occur when text differs"
    assert "assistant_canned_response_consumed" not in out[0]
    assert out[1]["text"] == "Sure, here's a thought."


def test_fold_does_not_fire_when_assistant_not_preceded_by_marker() -> None:
    """Negative Part A: 'No response requested.' from a regular conversation
    (not preceded by a marker) must NOT be folded.

    A user CAN legitimately receive this exact reply in a non-prelude context
    (e.g. mid-conversation `/exit` they later cancelled). Without this guard
    we'd silently erase real assistant content.
    """
    real_user = _post_msg("u1", "human", "Hello.", parent=None)
    canned = _post_msg("a1", "assistant", "No response requested.", parent="u1")

    out = _fold_canned_assistant_responses_into_marker([real_user, canned])

    assert len(out) == 2, "canned-response not preceded by a marker must survive"
    assert out[0]["text"] == "Hello."
    assert out[1]["text"] == "No response requested."


def test_fold_does_not_fire_when_assistant_has_tool_blocks() -> None:
    """Negative Part A: assistant whose text matches canned string BUT whose
    content carries `tool_use` is NOT folded.

    Tool history is real data — even if CC weirdly emits the canned text
    alongside a real tool call, we must NOT drop it. This is the
    Gemini-3-Pro-flagged "preserve tool history" guard.
    """
    marker = _marker("m1")
    weird_assistant = {
        "uuid": "a1",
        "sender": "assistant",
        "text": "No response requested.",
        "content": [
            {"type": "text", "text": "No response requested."},
            {"type": "tool_use", "name": "Bash", "input": {"command": "ls"}},
        ],
        "created_at": "2026-04-19T01:50:00Z",
        "updated_at": "2026-04-19T01:50:00Z",
        "truncated": False,
        "parent_message_uuid": "m1",
        "attachments": [],
        "files": [],
    }

    out = _fold_canned_assistant_responses_into_marker([marker, weird_assistant])

    assert len(out) == 2, "fold must refuse when content carries tool_use blocks"
    assert "assistant_canned_response_consumed" not in out[0]


def test_fold_handles_consecutive_canned_assistants() -> None:
    """Part A run-scan: a marker followed by TWO canned-response assistants
    (CC glitch) absorbs BOTH; successor's parent_message_uuid is remapped
    past the entire run.

    This is the Gemini-2.5-Pro-flagged correctness bug in the v1 draft.
    """
    marker = _marker("m1")
    canned1 = _post_msg("a1", "assistant", "No response requested.", parent="m1")
    canned2 = _post_msg("a2", "assistant", "No response requested.", parent="a1")
    real = _post_msg("u1", "human", "Real prompt.", parent="a2")

    out = _fold_canned_assistant_responses_into_marker([marker, canned1, canned2, real])

    assert len(out) == 2, (
        f"both canned-response assistants should be absorbed; got "
        f"{[m.get('text') for m in out]}"
    )
    assert out[0]["is_command_marker"] is True
    assert out[0]["assistant_canned_response_consumed"] is True
    # successor's parent was 'a2' (the last absorbed uuid); remapped to 'm1'.
    assert out[1]["parent_message_uuid"] == "m1", (
        f"successor's parent must be remapped past the entire absorbed run; "
        f"got {out[1]['parent_message_uuid']!r}"
    )
    assert out[1]["text"] == "Real prompt."


def test_fold_does_not_remap_when_successor_parent_is_unrelated() -> None:
    """Defensive: if the successor's parent_message_uuid does NOT point at
    any absorbed uuid (e.g. it's None for a root, or points elsewhere), the
    fold MUST NOT spuriously remap it. This catches the
    Gemini-2.5-Pro-flagged `None == None` false-positive.
    """
    marker = _marker("m1")
    canned = _post_msg("a1", "assistant", "No response requested.", parent="m1")
    # Successor's parent is None — could be a downstream-inserted root.
    real = _post_msg("u1", "human", "Real prompt.", parent=None)

    out = _fold_canned_assistant_responses_into_marker([marker, canned, real])

    assert len(out) == 2
    assert out[1]["parent_message_uuid"] is None, (
        "successor with parent=None must NOT be remapped (None==None guard)"
    )


def test_fold_handles_marker_with_no_successor() -> None:
    """Defensive: marker is the LAST entry; fold MUST NOT crash and MUST NOT
    set assistant_canned_response_consumed (nothing was absorbed).
    """
    marker = _marker("m1")

    out = _fold_canned_assistant_responses_into_marker([marker])

    assert len(out) == 1
    assert out[0]["is_command_marker"] is True
    assert "assistant_canned_response_consumed" not in out[0]


def test_fold_is_pure_functional_no_input_mutation() -> None:
    """Part A's contract: input list and member dicts are NEVER mutated.

    This is the Gemini-2.5-Pro-flagged "principle of least surprise" fix.
    A caller that retains a reference to the input list must see it
    unchanged after the call.
    """
    marker = _marker("m1")
    canned = _post_msg("a1", "assistant", "No response requested.", parent="m1")
    real = _post_msg("u1", "human", "Real prompt.", parent="a1")
    input_list = [marker, canned, real]

    # Snapshot of input dict ids so we can prove no in-place mutation.
    snapshot_ids = [id(m) for m in input_list]
    snapshot_keys = [tuple(sorted(m.keys())) for m in input_list]
    snapshot_parents = [m.get("parent_message_uuid") for m in input_list]

    _ = _fold_canned_assistant_responses_into_marker(input_list)

    # Input list length and member identity preserved.
    assert len(input_list) == 3
    assert [id(m) for m in input_list] == snapshot_ids
    # Input dict keys unchanged (no `assistant_canned_response_consumed` added).
    assert [tuple(sorted(m.keys())) for m in input_list] == snapshot_keys
    # Input dict parent_message_uuid values unchanged.
    assert [m.get("parent_message_uuid") for m in input_list] == snapshot_parents


def test_fold_is_idempotent() -> None:
    """Running the fold twice yields the same result as running it once.
    The second pass finds no marker→canned-response pair (the assistant
    is gone) so it's a no-op.
    """
    marker = _marker("m1")
    canned = _post_msg("a1", "assistant", "No response requested.", parent="m1")
    real = _post_msg("u1", "human", "Real prompt.", parent="a1")

    once = _fold_canned_assistant_responses_into_marker([marker, canned, real])
    twice = _fold_canned_assistant_responses_into_marker(once)

    assert len(once) == len(twice) == 2
    assert once[0]["assistant_canned_response_consumed"] is True
    assert twice[0]["assistant_canned_response_consumed"] is True
    assert once[1]["text"] == twice[1]["text"] == "Real prompt."


