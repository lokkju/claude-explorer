def test_prelude_flags_leading_markers_only() -> None:
    """Positive Part B: 2 leading markers + real user → count=2, both flagged,
    real user NOT flagged.
    """
    marker1 = _marker("m1")
    marker2 = _marker("m2")
    real = _post_msg("u1", "human", "Real prompt.", parent="m2")

    out, count = _flag_leading_prelude_markers([marker1, marker2, real])

    assert count == 2
    assert out[0]["is_prelude"] is True
    assert out[1]["is_prelude"] is True
    assert out[2].get("is_prelude", False) is False, (
        "real user message must NOT be flagged as prelude"
    )


def test_prelude_zero_when_session_starts_with_real_user() -> None:
    """Negative Part B: session starts with a real user message → count=0,
    no `is_prelude` flags anywhere.
    """
    real = _post_msg("u1", "human", "First real prompt.", parent=None)
    asst = _post_msg("a1", "assistant", "Reply.", parent="u1")

    out, count = _flag_leading_prelude_markers([real, asst])

    assert count == 0
    assert all(not m.get("is_prelude", False) for m in out)


def test_prelude_does_not_flag_mid_stream_marker() -> None:
    """Negative Part B: a marker AFTER the first real user message is NOT
    flagged. Only the LEADING run is the "session prelude" — mid-conversation
    /exit triplets should render inline.
    """
    real = _post_msg("u1", "human", "First real prompt.", parent=None)
    asst = _post_msg("a1", "assistant", "Reply.", parent="u1")
    mid_marker = _marker("m1", parent="a1")
    real2 = _post_msg("u2", "human", "Second prompt.", parent="m1")

    out, count = _flag_leading_prelude_markers([real, asst, mid_marker, real2])

    assert count == 0, "no LEADING markers → count must be 0"
    # Mid-stream marker still has is_command_marker but NOT is_prelude.
    assert out[2]["is_command_marker"] is True
    assert out[2].get("is_prelude", False) is False


def test_prelude_all_markers_session() -> None:
    """Degenerate session: only markers, no real messages. All flagged.

    This is the phantom-adjacent case — paired with the existing
    phantom-carveout invariant in `test_phantom_caveat_carveout.py`, it
    ensures the view is never blank.
    """
    marker1 = _marker("m1")
    marker2 = _marker("m2")

    out, count = _flag_leading_prelude_markers([marker1, marker2])

    assert count == 2
    assert all(m["is_prelude"] is True for m in out)


def test_prelude_is_pure_functional() -> None:
    """Part B's contract: input list and member dicts are never mutated."""
    marker = _marker("m1")
    real = _post_msg("u1", "human", "Real prompt.", parent="m1")
    input_list = [marker, real]
    snapshot_keys = [tuple(sorted(m.keys())) for m in input_list]

    _ = _flag_leading_prelude_markers(input_list)

    # Input dicts must not have gained `is_prelude` keys.
    assert [tuple(sorted(m.keys())) for m in input_list] == snapshot_keys


def test_prelude_is_idempotent() -> None:
    """Running the flag pass twice yields the same result as running it once."""
    marker = _marker("m1")
    real = _post_msg("u1", "human", "Real prompt.", parent="m1")

    once, c1 = _flag_leading_prelude_markers([marker, real])
    twice, c2 = _flag_leading_prelude_markers(once)

    assert c1 == c2 == 1
    assert once[0]["is_prelude"] is True
    assert twice[0]["is_prelude"] is True


